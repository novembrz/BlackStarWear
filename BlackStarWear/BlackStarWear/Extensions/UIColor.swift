extension UIColor {
    
    static func blackBGColor() -> UIColor{
        return #colorLiteral(red: 0.1450814903, green: 0.1451086402, blue: 0.1450755894, alpha: 1)
    }
    
    static func productCartColor() -> UIColor{
        return #colorLiteral(red: 0.2196078431, green: 0.2196078431, blue: 0.2196078431, alpha: 1)
    }
    
    static func redActionColor() -> UIColor{
        return #colorLiteral(red: 1, green: 0.3139866889, blue: 0.1870743334, alpha: 1)
    }
    
    
}
